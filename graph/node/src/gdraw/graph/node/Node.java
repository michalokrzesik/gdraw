package gdraw.graph.node;

import java.awt.geom.Point2D;

public class Node {
    private Point2D center;
    private double width;
    private double height;

    public Point2D getCenter() {
        return center;
    }

    public double getHeight() {
        return height;
    }

    public double getWidth() {
        return width;
    }
}
